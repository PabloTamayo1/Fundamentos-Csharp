// Parte 1

// 1. Declarar una variable de tipo int:
int numero = 10;

Console.WriteLine(numero + " el tipo de dato es: " + numero.GetType());


// 2. Declarar una variable de tipo double:
double valor = 10.5;

Console.WriteLine("\n" + valor + " el tipo de dato es: " + valor.GetType());


// 3. Declarar una variable de tipo string:
string nombre = "Pablo";

Console.WriteLine("\n" + nombre + " el tipo de dato es: " + nombre.GetType());


// 4. Declarar una variable utilizando la palabra clave var:
var num = 10;

Console.WriteLine("\n" + num + " el tipo de dato es: " + num.GetType());


// 5. Declarar una variable de tipo boolean:
bool verdadero = true;

Console.WriteLine("\n" + verdadero + " el tipo de dato es: " + verdadero.GetType());
